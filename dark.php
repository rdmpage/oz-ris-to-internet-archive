// Import reference and create identifier based on metadata
function archive_make_dark($identifier, $reason = "")
{
	global $config;

	$headers = array();

	// authorisation
	$headers[] = '"authorization: LOW ' . $config['s3_access_key']. ':' . $config['s3_secret_key'] . '"';

	// content
	$headers[] = '"content-type: application/json"';
	
	$obj = new stdclass;
	
	$obj->identifier = $identifier;
	$obj->cmd		 = "make_dark.php";
	$obj->args		 = new stdclass;
	
	if ($reason != "")
	{
		$obj->args->comment = $reason;
	}
	else
	{
		$obj->args->comment = "My mistake";
	}
	
	$command = 'curl -X POST';
	$command .= ' --header ' . join(' --header ', $headers);
	$command .= " -d '" . json_encode($obj) . "'";
	$command .= ' https://archive.org/services/tasks.php';

	echo $command . "\n";	
	
	system ($command);

}

